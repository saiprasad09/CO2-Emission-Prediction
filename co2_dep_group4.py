import pandas as pd
import streamlit as st
import numpy as np
import pickle
import warnings
warnings.filterwarnings('ignore')

model = pickle.load(open('co2_rf_model .pkl','rb'))

st.title(' CO2 prediction Model DEployment using Random forest regression')

feature_names = [
    "engine_size", "cylinders", "fuel_consumption_city",
    "fuel_consumption_hwy", "fuel_consumption_comb(l/100km)"
]

user_input = []
for feature in feature_names:
    user_input.append(st.sidebar.number_input(f"{feature}", value=0.0))

input_array = np.array(user_input).reshape(1, -1)

st.subheader("User Input Features")
st.write(pd.DataFrame([user_input], columns=feature_names))

prediction = model.predict(input_array)

st.subheader("Predicted COâ‚‚ Emissions")
st.write(f"**{prediction[0]:.2f} g/km**")